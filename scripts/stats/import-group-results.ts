// Reads ../../freshmen-random-house/team_result.csv (the offline group-preference
// random draw output) and generates a .sql file of UPDATE statements to apply
// teams.result_group_number in D1. Doesn't touch the DB directly - D1 is only
// reachable via a Workers binding or `wrangler d1 execute`, so this just
// produces the same kind of file packages/db's seed.sql/seed-checkin.sql use.
//
// Usage:
//   bun run import-group-results.ts
//   cd ../../packages/db && wrangler d1 execute DB --local --file=./import-group-results.sql --config ../../apps/web/wrangler.jsonc
//   (repeat with --remote once the local run looks right)

import parse from "csv-simple-parser";

type TeamResultRow = {
  final_team: string; // actually the team UUID, not a "final team" id - named this way in rando.ipynb
  result_group_number: string;
};

const csvPath = "../../../freshmen-random-house/team_result.csv";
const outPath = "../../packages/db/import-group-results.sql";

const file = Bun.file(csvPath);
if (!(await file.exists())) {
  console.error(`Not found: ${csvPath} - run freshmen-random-house/rando.ipynb first.`);
  process.exit(1);
}

const rows = parse((await file.text()).trim(), { header: true, delimiter: "," }).filter(Boolean) as TeamResultRow[];

const lines: string[] = [
  "-- Generated by scripts/stats/import-group-results.ts - do not hand-edit.",
  "-- Applies the offline group-preference random draw (freshmen-random-house/rando.ipynb) to teams.result_group_number.",
];

let skipped = 0;
for (const row of rows) {
  const teamId = row.final_team;
  const groupNumber = row.result_group_number;
  if (!teamId || !groupNumber) {
    skipped++;
    continue;
  }
  const escapedTeamId = teamId.replace(/'/g, "''");
  const escapedGroupNumber = groupNumber.replace(/'/g, "''");
  lines.push(
    `UPDATE teams SET result_group_number = (SELECT id FROM available_groups WHERE number = '${escapedGroupNumber}') WHERE id = '${escapedTeamId}';`,
  );
}

await Bun.write(outPath, lines.join("\n") + "\n");

console.log(`Wrote ${rows.length - skipped} UPDATE statements to ${outPath} (skipped ${skipped} incomplete rows).`);
